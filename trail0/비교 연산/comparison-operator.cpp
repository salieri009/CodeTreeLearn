#include <iostream>
using namespace std;

int main() {
    // 변수 선언
    int a, b;

    // 입력
    cin >> a >> b;

    // a >= b
    if (a >= b) cout << 1 << "\n";
    else cout << 0 << "\n";

    // a > b
    if (a > b) cout << 1 << "\n";
    else cout << 0 << "\n";

    // a <= b
    if (a <= b) cout << 1 << "\n";
    else cout << 0 << "\n";

    // a < b
    if (a < b) cout << 1 << "\n";
    else cout << 0 << "\n";

    // a == b
    if (a == b) cout << 1 << "\n";
    else cout << 0 << "\n";

    // a != b
    if (a != b) cout << 1 << "\n";
    else cout << 0 << "\n";

    return 0;
}
